/**
 * Phase 3: Fix JSX syntax errors left by the component migration.
 * Specifically fixes malformed template literal strings, unescaped braces, etc.
 * 
 * Run with: node fix-heroui-syntax.cjs
 */

const fs = require('fs');
const path = require('path');

const SRC_DIR = path.join(__dirname, 'src');

function getAllFiles(dir, exts = ['.tsx', '.ts']) {
  let results = [];
  const items = fs.readdirSync(dir, { withFileTypes: true });
  for (const item of items) {
    const fullPath = path.join(dir, item.name);
    if (item.isDirectory() && item.name !== 'node_modules' && item.name !== 'dist') {
      results = results.concat(getAllFiles(fullPath, exts));
    } else if (item.isFile() && exts.some(ext => item.name.endsWith(ext))) {
      results.push(fullPath);
    }
  }
  return results;
}

function fixSyntax(filePath) {
  let content = fs.readFileSync(filePath, 'utf8');
  const original = content;
  
  // Fix 1: Remove remaining {/* @ts-ignore */} before non-md elements
  content = content.replace(/\s*\{\/\*\s*@ts-ignore\s*\*\/\}\s*\n/g, '\n');
  
  // Fix 2: Remove any remaining <md-* tags that weren't caught
  // These would be syntax errors, so remove them with their closest closing tag
  
  // Fix 3: Remove "material-icon-placeholder" spans that weren't resolved
  content = content.replace(/<span className="material-icon-placeholder">[^<]*<\/span>/g, '');
  
  // Fix 4: Fix the md-primary-tab replacement issue - these left malformed button tags
  // The tabs generated by the script had issues with nested content containing special chars
  
  // Fix 5: Clean up any double-space lines and excessive blank lines
  content = content.replace(/\n{4,}/g, '\n\n');
  
  // Fix 6: Fix remaining md- tag fragments  
  // md-filled-tonal-button that wasn't caught (no closing tag on same pattern)
  content = content.replace(/<md-filled-tonal-button[^>]*>/g, '<button type="button" className="px-6 py-3 rounded-2xl font-semibold bg-[var(--app-surface-container-high)] text-[var(--app-foreground)] hover:bg-[var(--app-primary)] hover:text-[var(--app-primary-foreground)] transition-all shadow-sm">');
  content = content.replace(/<\/md-filled-tonal-button>/g, '</button>');
  
  // Any leftover md-icon-button
  content = content.replace(/<md-icon-button[^>]*>/g, '<button type="button" className="w-10 h-10 rounded-full flex items-center justify-center text-[var(--app-foreground)] hover:bg-[var(--app-surface-container)] transition-colors">');
  content = content.replace(/<\/md-icon-button>/g, '</button>');
  
  // Any leftover md-filled-tonal-icon-button
  content = content.replace(/<md-filled-tonal-icon-button[^>]*>/g, '<button type="button" className="w-full h-full rounded-2xl flex items-center justify-center bg-[var(--app-surface-container-high)] text-[var(--app-foreground)] hover:bg-[var(--app-primary)] hover:text-[var(--app-primary-foreground)] transition-colors shadow-sm">');
  content = content.replace(/<\/md-filled-tonal-icon-button>/g, '</button>');
  
  // Any leftover md-filled-icon-button 
  content = content.replace(/<md-filled-icon-button[^>]*>/g, '<button type="button" className="w-10 h-10 rounded-full flex items-center justify-center bg-[var(--app-primary)] text-[var(--app-primary-foreground)] hover:opacity-90 transition-all shadow-sm">');
  content = content.replace(/<\/md-filled-icon-button>/g, '</button>');
  
  // Any leftover md-outlined-icon-button
  content = content.replace(/<md-outlined-icon-button[^>]*>/g, '<button type="button" className="w-10 h-10 rounded-full flex items-center justify-center border-2 border-[var(--app-outline)]/20 text-[var(--app-foreground)] hover:bg-[var(--app-surface-container)] transition-colors">');
  content = content.replace(/<\/md-outlined-icon-button>/g, '</button>');
  
  // Any leftover md-filled-button
  content = content.replace(/<md-filled-button[^>]*>/g, '<button type="button" className="px-6 py-3 rounded-2xl font-semibold bg-[var(--app-primary)] text-[var(--app-primary-foreground)] hover:opacity-90 transition-all shadow-sm">');
  content = content.replace(/<\/md-filled-button>/g, '</button>');
  
  // Any leftover md-outlined-button
  content = content.replace(/<md-outlined-button[^>]*>/g, '<button type="button" className="px-6 py-3 rounded-2xl font-semibold border-2 border-[var(--app-outline)]/20 text-[var(--app-foreground)] hover:bg-[var(--app-surface-container)] transition-all">');
  content = content.replace(/<\/md-outlined-button>/g, '</button>');
  
  // Any leftover md-text-button
  content = content.replace(/<md-text-button[^>]*>/g, '<button type="button" className="px-4 py-2 rounded-xl font-semibold text-[var(--app-primary)] hover:bg-[var(--app-surface-container)] transition-colors">');
  content = content.replace(/<\/md-text-button>/g, '</button>');
  
  // Any leftover md-elevated-button
  content = content.replace(/<md-elevated-button[^>]*>/g, '<button type="button" className="px-6 py-3 rounded-2xl font-semibold bg-[var(--app-surface)] text-[var(--app-primary)] shadow-md hover:shadow-lg transition-all">');
  content = content.replace(/<\/md-elevated-button>/g, '</button>');
  
  // Any leftover md-filter-chip
  content = content.replace(/<md-filter-chip[^>]*>\s*<\/md-filter-chip>/g, '');
  
  // Any leftover md-tabs / md-primary-tab / md-secondary-tab
  content = content.replace(/<md-tabs[^>]*>/g, '<div className="flex border-b border-[var(--app-outline)]/10">');
  content = content.replace(/<\/md-tabs>/g, '</div>');
  content = content.replace(/<md-primary-tab[^>]*>/g, '<button type="button" className="flex-1 px-4 py-3 text-sm font-semibold text-center transition-colors">');
  content = content.replace(/<\/md-primary-tab>/g, '</button>');
  content = content.replace(/<md-secondary-tab[^>]*>/g, '<button type="button" className="flex-1 px-4 py-3 text-sm font-semibold text-center transition-colors">');
  content = content.replace(/<\/md-secondary-tab>/g, '</button>');
  
  // Any leftover md-switch
  content = content.replace(/<md-switch[^>]*>\s*<\/md-switch>/g, '<button type="button" role="switch" className="relative inline-flex h-7 w-12 items-center rounded-full transition-colors duration-200 bg-[var(--app-outline)]/30"><span className="inline-block h-5 w-5 transform rounded-full bg-white shadow-md transition-transform duration-200 translate-x-1" /></button>');
  
  // Any leftover md-slider
  content = content.replace(/<md-slider[^>]*>\s*<\/md-slider>/g, '<input type="range" className="w-full h-3 rounded-full appearance-none cursor-pointer accent-[var(--app-primary)] bg-[var(--app-surface-container-high)]" />');
  
  // Any leftover md-dialog
  content = content.replace(/<md-dialog[^>]*>/g, '<div className="fixed inset-0 z-50 flex items-center justify-center bg-black/50">');
  content = content.replace(/<\/md-dialog>/g, '</div>');
  
  // Any leftover md-circular-progress
  content = content.replace(/<md-circular-progress[^>]*>\s*<\/md-circular-progress>/g, '<div className="w-8 h-8 border-3 border-[var(--app-primary)]/30 border-t-[var(--app-primary)] rounded-full animate-spin" />');
  content = content.replace(/<md-circular-progress[^>]*>/g, '<div className="w-8 h-8 border-3 border-[var(--app-primary)]/30 border-t-[var(--app-primary)] rounded-full animate-spin">');
  content = content.replace(/<\/md-circular-progress>/g, '</div>');
  
  // Any leftover md-linear-progress
  content = content.replace(/<md-linear-progress[^>]*>\s*<\/md-linear-progress>/g, '<div className="w-full h-1 bg-[var(--app-primary)]/20 rounded-full"><div className="h-full bg-[var(--app-primary)] rounded-full animate-pulse w-1/2" /></div>');
  
  // Any leftover md-checkbox
  content = content.replace(/<md-checkbox[^>]*>\s*<\/md-checkbox>/g, '<input type="checkbox" className="w-5 h-5 rounded accent-[var(--app-primary)] cursor-pointer" />');
  
  // Any leftover md-divider
  content = content.replace(/<md-divider[^>]*>\s*<\/md-divider>/g, '<hr className="border-[var(--app-outline)]/10" />');
  content = content.replace(/<md-divider[^>]*\/>/g, '<hr className="border-[var(--app-outline)]/10" />');
  
  // Any leftover md-ripple
  content = content.replace(/<md-ripple[^>]*>\s*<\/md-ripple>/g, '');
  content = content.replace(/<md-ripple[^>]*\/>/g, '');
  content = content.replace(/<md-ripple>/g, '');
  content = content.replace(/<\/md-ripple>/g, '');
  
  // Any leftover md-elevation
  content = content.replace(/<md-elevation[^>]*>\s*<\/md-elevation>/g, '');
  content = content.replace(/<md-elevation[^>]*\/>/g, '');
  
  // Any leftover md-icon
  content = content.replace(/<md-icon[^>]*>[^<]*<\/md-icon>/g, '');
  
  // Any leftover md-list / md-list-item
  content = content.replace(/<md-list[^>]*>/g, '<div className="flex flex-col gap-2">');
  content = content.replace(/<\/md-list>/g, '</div>');
  content = content.replace(/<md-list-item[^>]*>/g, '<div className="px-4 py-3 rounded-2xl hover:bg-[var(--app-surface-container-high)] transition-colors">');
  content = content.replace(/<\/md-list-item>/g, '</div>');
  
  // md-outlined-text-field leftover
  content = content.replace(/<md-outlined-text-field[^>]*>/g, '<input type="text" className="w-full px-4 py-3 rounded-2xl border-2 border-[var(--app-outline)]/20 bg-[var(--app-surface-container)] text-[var(--app-foreground)] focus:border-[var(--app-primary)] focus:outline-none transition-colors" />');
  content = content.replace(/<\/md-outlined-text-field>/g, '');
  
  // md-outlined-select and md-select-option
  content = content.replace(/<md-outlined-select[^>]*>/g, '<select className="px-4 py-3 rounded-2xl border-2 border-[var(--app-outline)]/20 bg-[var(--app-surface-container)] text-[var(--app-foreground)]">');
  content = content.replace(/<\/md-outlined-select>/g, '</select>');
  content = content.replace(/<md-select-option/g, '<option');
  content = content.replace(/<\/md-select-option>/g, '</option>');
  
  if (content !== original) {
    fs.writeFileSync(filePath, content, 'utf8');
    return true;
  }
  return false;
}

const files = getAllFiles(SRC_DIR);
let modified = 0;
for (const file of files) {
  if (fixSyntax(file)) {
    modified++;
    console.log(`  ✓ ${path.relative(SRC_DIR, file)}`);
  }
}
console.log(`\nSyntax fix: ${modified}/${files.length} files modified.`);
